function do_live_search(search_str){
	var search_type = document.getElementById("search_type").value;
	var col_name;
	var tab_name;
	var return_parameter;
	switch(search_type){
		case "1":
				col_name = "prod_name";
				tab_name = "prod_mast";
				return_parameter = "prod_name";
			break;
		case "2":
				col_name = "level_name";
				tab_name = "levels";
				return_parameter = "level_name";
			break;
		case "3":
				col_name = "brand_name";
				tab_name = "brand_mast";
				return_parameter = "brand_name";
			break;
		default:	
				col_name = "prod_name";
				tab_name = "prod_mast";
				return_parameter = "prod_name";	
	}
	var result = call_ajax("<?=$url_root?>ajax.php","process=live_search&search_str=" + search_str + "&tab_name=" + tab_name + "&col_name=" + col_name + "&return_parameter=" + return_parameter);
	if(result=="false") return "";
	return result;
}
